
public class concepts {
    public static void main(String[] args)
        {
        // Integers have no decimal values:
        int anIntegerValue = 0;
        // Floats have decimal values- ensure you add f at the end:
        float aFloatValue = 10.987f;
        //Character variables contain one letter
        char aLetter = 'A';
        //Booleans are either True or False - syntax in JAVA
        //requires you type your boolean terms in undercase:
        boolean boolTrue = true; 
        boolean boolFalse = false;
        //Strings in JAVA can hold more than one char value:
        String setOfLetters = "A, B, C, D";
        String varJava = "VARIABLES IN JAVA: ";
        
        //PRINT VARIABLES:
        System.out.println();
        System.out.println();
        System.out.println(varJava);
        System.out.println();
        System.out.println("INTEGERS:");
        System.out.println(anIntegerValue);
        System.out.println();
        System.out.println("FLOATS:");
        System.out.println(aFloatValue);
        System.out.println();
        System.out.println("CHAR:");
        System.out.println(aLetter);
        System.out.println();
        System.out.println("BOOLEANS:");
        System.out.println(boolTrue);
        System.out.println(boolFalse);
        System.out.println();
        System.out.println("STRING:");
        System.out.println(setOfLetters);
        }
}